"""
String Array Example
Demonstrates creating and iterating through an array of strings.
"""


def main():
    """Create an array of names and print each one."""
    names = ["John", "Mary", "David", "Sarah", "Emily", "Michael", "Jessica", "William", "Olivia", "James"]

    for name in names:
        print(name)


if __name__ == "__main__":
    main()
