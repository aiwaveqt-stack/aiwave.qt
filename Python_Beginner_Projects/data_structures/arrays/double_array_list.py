"""
Double ArrayList Example
Demonstrates using a list to store and display double values.
"""


def main():
    """Create a list of doubles and print them."""
    doubles = []
    doubles.append(10.5)
    doubles.append(20.2)
    doubles.append(30.8)
    doubles.append(40.1)
    doubles.append(50.9)

    print(f"Doubles: {doubles}")


if __name__ == "__main__":
    main()
