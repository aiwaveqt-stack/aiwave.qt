"""
String ArrayList Example
Demonstrates using a list to store and display string values.
"""


def main():
    """Create a list of names and print them."""
    names = []
    names.append("John")
    names.append("Mike")
    names.append("Ama")
    names.append("Daniel")

    print(f"Names: {names}")


if __name__ == "__main__":
    main()
